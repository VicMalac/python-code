
# bool: Valores booleanos retornam apenas dois valores, True e False, mas também pode ser 0 e 1, depende do programa

# Tipo de dado bool (boolean)
# Ao questionar algo em um programa,
# só existem duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro

print(1 > 2) # bool faz a pergunta, 1 é maior que 2? Que no caso retorna falso, já que ele é menor que 2
print(type(1 > 2)) # bool
print(12 == 12)
print(12 == 13)