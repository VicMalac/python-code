"""
    Tipo primitivo de dados

    int: Número inteiro (1, 2, 3, 4...)
    float: Numeros decimais/reais (1.21, 2.6, 71.5, 10.0, ...)
    str: texto ("Aninha tem cabelos bonitos")
"""

# Função type imprime o tipo da variável
# Control + ; adiciona comentários automaticamente a linhas selecionadas

print(123) # int
print(type(123)) # int

print(type(43.51)) # float
print(43.51) # float

print("Eu gosto de amar") # str
print(type("Eu gosto de amar")) # str